package ru.job4j.loop;

/**
 * Демонстрация for
 */
public class LoopFor {
    public static void main(String[] args) {
        for (int index = 0; index <= 5; index++) {
            System.out.println(index);
        }
        System.out.println("\nTask");
        for (int i = 5; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
